class Solution:

    def minSum(self, triangle: List[List[int]], level: int, i: int) -> int:
        size = len(triangle)
        for i in range(size - 2, -1, -1):
            rowSize = len(triangle[i])
            for j in range(rowSize):
                # print("level: %s i: %s j: %s" % (level, i, j))
                triangle[i][j] = triangle[i][j] + min(triangle[i+1][j], triangle[i+1][j+1])
        print(triangle)
        return triangle[0][0]
    
    def minimumTotal(self, triangle: List[List[int]]) -> int:
        return self.minSum(triangle, 0, 0)


